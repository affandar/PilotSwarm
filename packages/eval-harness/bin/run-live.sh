#!/usr/bin/env bash
#
# packages/eval-harness/bin/run-live.sh
#
# Convenience wrapper for the LIVE-gated eval-harness suites. Maps
# named flags onto the env-var gates documented in
# `packages/eval-harness/.env.example`, picks a timestamped
# `.eval-results/<ts>/` reports dir by default, and forwards the rest
# to `vitest run`.
#
# Examples
# --------
#   # All live suites with default credentials, no judge, no heavy perf
#   bin/run-live.sh
#
#   # Everything (heavy + n8 concurrency + durability perf + pg_stat + judge)
#   bin/run-live.sh --all
#
#   # Just the judge + safety judge cases
#   bin/run-live.sh --judge -- test/llm-judge-live.test.ts test/safety-live.test.ts
#
#   # Performance only (no judge cost)
#   bin/run-live.sh --perf
#
#   # Cheap smoke (live, no judge, no perf)
#   bin/run-live.sh --cheap
#
# Run from monorepo root or from packages/eval-harness/. Either works.

set -euo pipefail

# --- Locate eval-harness package root regardless of CWD --------------
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PKG_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"

# --- Defaults --------------------------------------------------------
LIVE=1
LIVE_JUDGE=0
PERF_HEAVY=0
PERF_HEAVY_N8=0
PERF_DURABILITY=0
PG_STAT_STATEMENTS_ENABLED=0
PROMPT_TESTING=0
KEEP_DURABILITY_ENV=0
EVAL_VERBOSE_TEARDOWN=0
PS_EVAL_FILE_PARALLELISM=0
GENERATE_REPORT=1        # default ON — every LIVE run consolidates
                         # jsonl into REPORT-<ts>.md; opt out with --no-report
REPORT_OUT=""            # explicit override path; empty → write inside reports dir

REPORTS_DIR=""           # empty → auto-generate timestamped dir
NO_REPORTS=0
TEST_GLOB_DEFAULT=("test/*-live.test.ts")
TESTS=()
VITEST_PASSTHRU=()
SAW_DOUBLE_DASH=0

usage() {
  cat <<'USAGE'
Usage: run-live.sh [flags] [-- <vitest-args> | <test-files>...]

Suite gates (set the matching env var to 1 for the run):
  --live                LIVE=1              (default ON; --no-live disables)
  --no-live             Skip LIVE gate (mostly useful for dry-runs)
  --judge               LIVE_JUDGE=1
  --heavy               PERF_HEAVY=1        (concurrency profiler ≥7 sessions)
  --heavy-n8            PERF_HEAVY=1 + PERF_HEAVY_N8=1 (≥15 sessions)
  --durability-perf     PERF_DURABILITY=1
  --pg-stat             PG_STAT_STATEMENTS_ENABLED=1
  --prompt-testing      PROMPT_TESTING=1

Behavior knobs:
  --keep-env            KEEP_DURABILITY_ENV=1
  --verbose-teardown    EVAL_VERBOSE_TEARDOWN=1
  --parallel-files      PS_EVAL_FILE_PARALLELISM=1
                        Run *-live.test.ts files in parallel. Default OFF
                        because each file opens SDK pg pools and the local
                        Postgres saturates at max_connections=200 with ~3
                        parallel files. Only flip this on if you've bumped
                        max_connections (or know what you're doing).
  --reports-dir <path>  EVAL_REPORTS_DIR=<path>   (relative to PKG_DIR)
  --no-reports          do not auto-create EVAL_REPORTS_DIR (also disables
                        the consolidated Markdown report — nothing to read)
  --report-out <path>   write the consolidated Markdown report to this
                        explicit path instead of <reports-dir>/REPORT-<ts>.md
  --no-report           skip the consolidated Markdown report (the report
                        is generated by default after every LIVE run)

Grouped flags:
  --all                 judge + heavy + heavy-n8 + durability-perf + pg-stat
  --perf                heavy + heavy-n8 + durability-perf + pg-stat
  --cheap               live only (clears any prior --judge/--heavy/...)

Misc:
  --dry-run             print resolved env + vitest invocation; exit 0
  -h, --help            this help

Anything after `--` is forwarded verbatim to vitest. Bare positional
arguments are treated as test-file paths (defaults to test/*-live.test.ts).
USAGE
}

apply_all() {
  LIVE_JUDGE=1
  PERF_HEAVY=1
  PERF_HEAVY_N8=1
  PERF_DURABILITY=1
  PG_STAT_STATEMENTS_ENABLED=1
}

apply_perf() {
  PERF_HEAVY=1
  PERF_HEAVY_N8=1
  PERF_DURABILITY=1
  PG_STAT_STATEMENTS_ENABLED=1
}

apply_cheap() {
  LIVE_JUDGE=0
  PERF_HEAVY=0
  PERF_HEAVY_N8=0
  PERF_DURABILITY=0
  PG_STAT_STATEMENTS_ENABLED=0
  PROMPT_TESTING=0
}

DRY_RUN=0

while (( $# > 0 )); do
  if (( SAW_DOUBLE_DASH == 1 )); then
    VITEST_PASSTHRU+=("$1"); shift; continue
  fi
  case "$1" in
    -h|--help) usage; exit 0 ;;
    --live) LIVE=1 ;;
    --no-live) LIVE=0 ;;
    --judge) LIVE_JUDGE=1 ;;
    --heavy) PERF_HEAVY=1 ;;
    --heavy-n8) PERF_HEAVY=1; PERF_HEAVY_N8=1 ;;
    --durability-perf) PERF_DURABILITY=1 ;;
    --pg-stat) PG_STAT_STATEMENTS_ENABLED=1 ;;
    --prompt-testing) PROMPT_TESTING=1 ;;
    --keep-env) KEEP_DURABILITY_ENV=1 ;;
    --verbose-teardown) EVAL_VERBOSE_TEARDOWN=1 ;;
    --parallel-files) PS_EVAL_FILE_PARALLELISM=1 ;;
    --no-report) GENERATE_REPORT=0 ;;
    --report-out)
      shift; [[ $# -gt 0 ]] || { echo "--report-out needs a path" >&2; exit 2; }
      REPORT_OUT="$1" ;;
    --report-out=*) REPORT_OUT="${1#*=}" ;;
    --reports-dir)
      shift; [[ $# -gt 0 ]] || { echo "--reports-dir needs a path" >&2; exit 2; }
      REPORTS_DIR="$1" ;;
    --reports-dir=*) REPORTS_DIR="${1#*=}" ;;
    --no-reports) NO_REPORTS=1 ;;
    --all) apply_all ;;
    --perf) apply_perf ;;
    --cheap) apply_cheap ;;
    --dry-run) DRY_RUN=1 ;;
    --) SAW_DOUBLE_DASH=1 ;;
    -*)
      echo "unknown flag: $1" >&2; usage >&2; exit 2 ;;
    *)
      TESTS+=("$1") ;;
  esac
  shift
done

# Default test glob if no positional / no passthrough file given.
# Expand the glob from inside PKG_DIR so vitest receives concrete file
# paths — vitest 4 treats unmatched glob args as literal filters and
# exits "No test files found" if the shell never expanded them.
if (( ${#TESTS[@]} == 0 )); then
  pushd "${PKG_DIR}" >/dev/null
  shopt -s nullglob
  TESTS=( ${TEST_GLOB_DEFAULT[@]} )
  shopt -u nullglob
  popd >/dev/null
  if (( ${#TESTS[@]} == 0 )); then
    echo "no live test files matched ${TEST_GLOB_DEFAULT[*]} under ${PKG_DIR}" >&2
    exit 1
  fi
fi

# Reports dir resolution
if (( NO_REPORTS == 0 )); then
  if [[ -z "${REPORTS_DIR}" ]]; then
    REPORTS_DIR=".eval-results/$(date +%Y%m%d-%H%M%S)"
  fi
fi

# Build env exports
declare -a ENV_PAIRS=()
push_env() {
  local k="$1" v="$2"
  ENV_PAIRS+=("${k}=${v}")
}

(( LIVE == 1 )) && push_env LIVE 1
(( LIVE_JUDGE == 1 )) && push_env LIVE_JUDGE 1
(( PERF_HEAVY == 1 )) && push_env PERF_HEAVY 1
(( PERF_HEAVY_N8 == 1 )) && push_env PERF_HEAVY_N8 1
(( PERF_DURABILITY == 1 )) && push_env PERF_DURABILITY 1
(( PG_STAT_STATEMENTS_ENABLED == 1 )) && push_env PG_STAT_STATEMENTS_ENABLED 1
(( PROMPT_TESTING == 1 )) && push_env PROMPT_TESTING 1
(( KEEP_DURABILITY_ENV == 1 )) && push_env KEEP_DURABILITY_ENV 1
(( EVAL_VERBOSE_TEARDOWN == 1 )) && push_env EVAL_VERBOSE_TEARDOWN 1
(( PS_EVAL_FILE_PARALLELISM == 1 )) && push_env PS_EVAL_FILE_PARALLELISM 1
(( NO_REPORTS == 0 )) && push_env EVAL_REPORTS_DIR "${REPORTS_DIR}"

if (( DRY_RUN == 1 )); then
  echo "PKG_DIR:        ${PKG_DIR}"
  echo "Env injected:"
  for kv in "${ENV_PAIRS[@]}"; do echo "  ${kv}"; done
  echo "Vitest:"
  echo "  npx vitest run ${TESTS[*]} ${VITEST_PASSTHRU[*]+${VITEST_PASSTHRU[*]}}"
  exit 0
fi

cd "${PKG_DIR}"

# Write a run-meta sidecar so `bin/report.mjs` can render the suite
# gates / judge model / DB host even when it's invoked AFTER vitest has
# exited (env at that point no longer carries LIVE / LIVE_JUDGE / etc.).
# We capture only the gate vars and presence-only flags for secrets — no
# secret values, no full env dump.
write_run_meta() {
  local target="$1"
  [[ -d "${target}" ]] || mkdir -p "${target}" 2>/dev/null || return 0
  local meta="${target}/RUN-META.json"
  local started_at
  started_at="$(date -u +%Y-%m-%dT%H:%M:%SZ)"
  local db_host=""
  if [[ -n "${DATABASE_URL:-}" ]]; then
    # Extract host:port portion only — never the password
    db_host="$(printf '%s' "${DATABASE_URL}" | sed -E 's,.*@([^/?]+).*,\1,' | head -c 100)"
  fi
  cat > "${meta}" <<JSON
{
  "schemaVersion": 1,
  "startedAt": "${started_at}",
  "wrapper": "bin/run-live.sh",
  "pkgDir": "${PKG_DIR}",
  "reportsDir": "${target}",
  "gates": {
    "LIVE": "${LIVE}",
    "LIVE_JUDGE": "${LIVE_JUDGE}",
    "PERF_HEAVY": "${PERF_HEAVY}",
    "PERF_HEAVY_N8": "${PERF_HEAVY_N8}",
    "PERF_DURABILITY": "${PERF_DURABILITY}",
    "PG_STAT_STATEMENTS_ENABLED": "${PG_STAT_STATEMENTS_ENABLED}",
    "PROMPT_TESTING": "${PROMPT_TESTING}",
    "PS_EVAL_FILE_PARALLELISM": "${PS_EVAL_FILE_PARALLELISM}",
    "KEEP_DURABILITY_ENV": "${KEEP_DURABILITY_ENV}",
    "EVAL_VERBOSE_TEARDOWN": "${EVAL_VERBOSE_TEARDOWN}"
  },
  "models": {
    "LIVE_JUDGE_MODEL": "${LIVE_JUDGE_MODEL:-}",
    "LIVE_JUDGE_MODEL_A": "${LIVE_JUDGE_MODEL_A:-}",
    "LIVE_JUDGE_MODEL_B": "${LIVE_JUDGE_MODEL_B:-}",
    "LIVE_MATRIX_MODELS": "${LIVE_MATRIX_MODELS:-}",
    "LIVE_ABLATION_MODELS": "${LIVE_ABLATION_MODELS:-}",
    "PROMPT_TESTING_MODELS": "${PROMPT_TESTING_MODELS:-}"
  },
  "infra": {
    "DATABASE_HOST": "${db_host}",
    "GITHUB_TOKEN_PRESENT": "$([[ -n "${GITHUB_TOKEN:-}" ]] && echo yes || echo no)",
    "OPENAI_API_KEY_PRESENT": "$([[ -n "${OPENAI_API_KEY:-}" ]] && echo yes || echo no)",
    "PS_MODEL_PROVIDERS_PATH": "${PS_MODEL_PROVIDERS_PATH:-}"
  }
}
JSON
}

if (( NO_REPORTS == 0 )); then
  RESOLVED_REPORTS_DIR=""
  if [[ -n "${REPORTS_DIR}" ]]; then
    if [[ "${REPORTS_DIR}" = /* ]]; then
      RESOLVED_REPORTS_DIR="${REPORTS_DIR}"
    else
      RESOLVED_REPORTS_DIR="${PKG_DIR}/${REPORTS_DIR}"
    fi
    write_run_meta "${RESOLVED_REPORTS_DIR}"
  fi
fi

# Ensure dist/ is fresh — `npx vitest run` skips the npm `pretest` hook,
# so without this a stale dist/ can mask source changes (the package
# exposes `dist/index.js` via the `.` export). Skip with EVAL_SKIP_BUILD=1
# if you've just built and want to shave the tsc cost.
if [[ "${EVAL_SKIP_BUILD:-0}" != "1" ]]; then
  npm run --silent build
fi

# Reports + Markdown generation flow:
#
#   * If --no-reports, vitest runs without EVAL_REPORTS_DIR and there's
#     nothing to consolidate; skip Markdown report regardless of flags.
#   * If --no-report, run vitest exec-style (replace process) — fastest
#     path with no post-processing.
#   * Default: run vitest, capture exit code, generate consolidated
#     Markdown into <reports-dir>/REPORT-<ts>.md (or --report-out path),
#     exit with vitest's code so CI gating still fires.

# shellcheck disable=SC2068
if (( GENERATE_REPORT == 0 )) || (( NO_REPORTS == 1 )); then
  exec env ${ENV_PAIRS[@]} npx vitest run "${TESTS[@]}" ${VITEST_PASSTHRU[@]+"${VITEST_PASSTHRU[@]}"}
fi

set +e
env ${ENV_PAIRS[@]} npx vitest run "${TESTS[@]}" ${VITEST_PASSTHRU[@]+"${VITEST_PASSTHRU[@]}"}
VITEST_EXIT=$?
set -e

REPORT_TARGET=""
if [[ -n "${REPORTS_DIR}" ]]; then
  if [[ "${REPORTS_DIR}" = /* ]]; then
    REPORT_TARGET="${REPORTS_DIR}"
  else
    REPORT_TARGET="${PKG_DIR}/${REPORTS_DIR}"
  fi
fi

if [[ -d "${REPORT_TARGET}" ]]; then
  if [[ -n "${REPORT_OUT}" ]]; then
    REPORT_PATH="$(node "${PKG_DIR}/bin/report.mjs" "${REPORT_TARGET}" --out "${REPORT_OUT}")" || true
  else
    REPORT_PATH="$(node "${PKG_DIR}/bin/report.mjs" "${REPORT_TARGET}")" || true
  fi
  if [[ -n "${REPORT_PATH}" ]]; then
    echo ""
    echo "Report: ${REPORT_PATH}"
  fi
else
  echo "[run-live.sh] reports dir '${REPORT_TARGET}' not found; skipping Markdown report." >&2
fi

exit "${VITEST_EXIT}"
